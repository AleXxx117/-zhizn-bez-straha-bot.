def normalize(text: str) -> str:
    return " ".join(text.lower().replace("ё", "е").split())


def classify(text: str) -> str:
    normalized = normalize(text)
    for pattern in CRISIS_PATTERNS:
        if re.search(pattern, normalized, re.IGNORECASE):
            return "crisis"
    for pattern in MEDICAL_EMERGENCY_PATTERNS:
        if re.search(pattern, normalized, re.IGNORECASE):
            return "medical_emergency"
    for intent, patterns in INTENT_PATTERNS:
        if any(re.search(pattern, normalized, re.IGNORECASE) for pattern in patterns):
            return intent
    return "unknown"


def pick_response(intent: str, variation_key: str) -> str:
    choices = RESPONSES.get(intent, RESPONSES["unknown"])
    digest = hashlib.sha256(f"{intent}:{variation_key}".encode("utf-8")).digest()
    return choices[int.from_bytes(digest[:4], "big") % len(choices)]


def daily_message(chat_id: int, today: date) -> str:
    key = f"{chat_id}:{today.isoformat()}"
    digest = hashlib.sha256(key.encode("utf-8")).digest()
    return DAILY_MESSAGES[int.from_bytes(digest[:4], "big") % len(DAILY_MESSAGES)]


